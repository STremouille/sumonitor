public class NormalMode extends MilestoneMode {

	Color nonOperatedColor = new Color(255,106,106);//indian red 1
	Color operatedColor = new Color(171,255,171);
	
	@Override
	public Color getBorderColor() {
		return Color.black;
	}

	@Override
	public Color getTitleColor() {
		return new Color(255, 171, 171);
	}

	@Override
	public Color getDescriptionColor() {
		return new Color(255, 255, 171);
	}

	@Override
	public Color getProgressRectangleBackgroundColor() {
		return Color.white;
	}
	
	@Override
	public Color getProgressBarBackgroundColor() {
		return Color.white;
	}


	@Override
	public Color getDateColor() {
		return new Color(255,213,171);
	}

	@Override
	public Color getTextColor() {
		return Color.black;
	}
	
	@Override
	public Color getOperatedColor(){
		return operatedColor;
	}
	
	/**
	 * @return NonOperatedColor
	 */
	public Color getNonOperatedColor(){
		return nonOperatedColor;
	}

	@Override
	public Color getShadowColor() {
		return Color.LIGHT_GRAY;
	}

}
